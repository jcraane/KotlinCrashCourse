package dev.jamiecraane.kcc.coursecontents.basicsyntax

fun main() {
    val singleChar = 'a'
    println(singleChar)

    val digit = '9'.digitToInt()
    println(digit)
}

private fun standardChars() {
    val tab = '\t'
    val backspace = '\b'
    val newLine = '\n'
    val cr = '\r'
    val singleQuotationMark = '\''
    val doubleQuotationMark = '\"'
    val backSlash = '\\'
    val dollarSign = '\$'
}
